package model;

/**
 *
 * @author Hoa
 */
public class Image {
    private int id;
    private String pid,url;

    public Image() {
    }

    public Image(int id, String pid, String url) {
        this.id = id;
        this.pid = pid;
        this.url = url;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
    
}
